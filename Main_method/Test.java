package Main_method;

public class Test {

	public static void main(String[] args) {
		System.out.println("public: It's an access specifier that allows the method to be accessible from anywhere. public means the method can be accessed by any other class.\r\n");
		System.out.println("static: It indicates that the method belongs to the class itself rather than to a specific instance of the class. This allows the JVM to call the main method without creating an instance of the class.\r\n");
		System.out.println("void: It specifies that the main method does not return any value. In Java, void means the method doesn't return anything.\r\n");
		System.out.println("main: It's the name of the method. This is the conventional name recognized by the JVM as the starting point of a Java program.\r\n");
		System.out.println("String[] args: It represents the parameters that the main method accepts. It's an array of strings that can hold command-line arguments passed to the program when it's executed.");
	}

}
