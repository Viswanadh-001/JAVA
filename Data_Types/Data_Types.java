package Data_Types;
class Data_Types {
        // primitive data types : 8 types.
	public static void main(String[] args) {
	     byte a = 100;    // byte data type
	     short b = 32767; // short data type
	     int c = 1;       // int data type
	     long d = 976579876; // long data type
	     float e = 55f;   // float data type
	     double f = 4.6; // double data type
	     char g = 'A';  // char data type
	     boolean h = true; //boolean data type
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
	}

}
